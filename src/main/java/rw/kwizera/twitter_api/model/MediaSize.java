package rw.kwizera.twitter_api.model;

public class MediaSize {

    private Size thumb;

    private Size large;

    private Size medium;

    private Size small;

    public Size getThumb() {
        return thumb;
    }

    public void setThumb(Size thumb) {
        this.thumb = thumb;
    }

    public Size getLarge() {
        return large;
    }

    public void setLarge(Size large) {
        this.large = large;
    }

    public Size getMedium() {
        return medium;
    }

    public void setMedium(Size medium) {
        this.medium = medium;
    }

    public Size getSmall() {
        return small;
    }

    public void setSmall(Size small) {
        this.small = small;
    }
}
